const mongoose = require("mongoose");

const subscriptionSchema = new mongoose.Schema(
  {
    sectionpath: {
      type: String,
      required: true,
    },
    sectionname: {
      type: String,
      required: true,
    },
    picture: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

// model subscription
const subscription = new mongoose.model("subscription", subscriptionSchema);
module.exports = subscription;
