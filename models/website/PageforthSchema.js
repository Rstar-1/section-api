const mongoose = require("mongoose");

const pforthSchema = new mongoose.Schema(
  {
    sectionpath: {
      type: String,
      required: true,
    },
    sectionname: {
      type: String,
      required: true,
    },
    picture: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

// model pforth
const pforth = new mongoose.model("pforth", pforthSchema);
module.exports = pforth;
