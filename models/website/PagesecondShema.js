const mongoose = require("mongoose");

const psecondSchema = new mongoose.Schema(
  {
    sectionpath: {
      type: String,
      required: true,
    },
    sectionname: {
      type: String,
      required: true,
    },
    picture: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

// model psecond
const psecond = new mongoose.model("psecond", psecondSchema);
module.exports = psecond;
