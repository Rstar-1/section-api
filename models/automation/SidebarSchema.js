const mongoose = require("mongoose");

const sidebarSchema = new mongoose.Schema(
  {
    sectionpath: {
      type: String,
      required: true,
    },
    sectionname: {
      type: String,
      required: true,
    },
    picture: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

// model sidebar
const sidebar = new mongoose.model("sidebar", sidebarSchema);
module.exports = sidebar;
