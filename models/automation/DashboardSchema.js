const mongoose = require("mongoose");

const dashboardSchema = new mongoose.Schema(
  {
    sectionpath: {
      type: String,
      required: true,
    },
    sectionname: {
      type: String,
      required: true,
    },
    picture: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

// model dashboard
const dashboard = new mongoose.model("dashboard", dashboardSchema);
module.exports = dashboard;
